function cong(a,b){
    return a+b;
}
function tru(a,b){
    return a-b;
}
function nhan(a,b){
    return a*b;
}
function chia(a,b){
    return a/b;
}
function canBacHai(x){
    return Math.sqrt(x);
}
function luyThua(x,y){
    return Math.pow(x,y);
}

console.log(cong(5, 3)); 
console.log(tru(10, 2)); 
console.log(nhan(4, 6)); 
console.log(chia(8, 4)); 

console.log(canBacHai(25)); 
console.log(canBacHai(9)); 


